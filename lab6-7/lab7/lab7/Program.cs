/*
Алгоритм

1. Сначала нужно создать два списка чисел одинаковой длины — a и b.

2. Проверить, что в обоих списках одинаковое количество элементов.
   Если количество не совпадает, нужно вывести сообщение об ошибке и остановить выполнение.

3. Создать пустой список result, в который будут записываться суммы чисел.

4. Посчитать суммы элементов из двух списков с помощью цикла while.
   Пока индекс меньше длины списка, брать по одному числу из каждого списка, складывать их и записывать в новый список.

5. После завершения цикла вывести результат сложения, полученный с помощью while.

6. Затем снова пройти по спискам с помощью цикла for.
   На каждом шаге брать по одному числу из каждого списка, складывать и записывать сумму в тот же список result.

7. После этого вывести результат сложения, полученный с помощью for.

8. Когда оба результата выведены, программа завершается.

*/

class Program
{
    static void Main()
    {
        int[] a = { 1, 2, 3, 4 };
        int[] b = { 5, 6, 7, 8 };

        if (a.Length != b.Length)
        {
            Console.WriteLine("Длины массивов должны совпадать");
            return;
        }

        int[] result = new int[a.Length];

        SumPairwiseWhile(a, b, result);
        Console.WriteLine("Результат (while): " + string.Join(" ", result));

        SumPairwiseFor(a, b, result);
        Console.WriteLine("Результат (for): " + string.Join(" ", result));
    }

    static void SumPairwiseWhile(int[] a, int[] b, int[] result)
    {
        int i = 0;
        while (i < a.Length)
        {
            result[i] = a[i] + b[i];
            i++;
        }
    }

    static void SumPairwiseFor(int[] a, int[] b, int[] result)
    {
        for (int i = 0; i < a.Length; i++)
        {
            result[i] = a[i] + b[i];
        }
    }
}
